    notes = load_notes()
    if notes.get("entries"):
        base += "\n\n--- NOTES FROM PREVIOUS RUNS ---\n"
        base += "Use these to skip exploration and go straight to working code:\n\n"
        for entry in notes["entries"]:
            base += f"[{entry['timestamp']}]\n{entry['notes']}\n\n"
        base += "--- END NOTES ---\n"
        base += "\nIf the website structure hasn't changed, reuse the approach above directly.\n"

