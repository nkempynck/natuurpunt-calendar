# ── Agentic loop ─────────────────────────────────────────────────────────────

def run_agent():
    print("=" * 60)
    print("\U0001f33f Natuurpunt Vlaams-Brabant Agenda Agent")
    print("=" * 60)
    print()

    # Load memory from previous runs
    notes = load_notes()
    if notes.get("entries"):
        print(f"\U0001f9e0 Loaded {len(notes['entries'])} note(s) from previous runs")
        print(f"   Last run: {notes['entries'][-1]['timestamp']}")
    else:
        print("\U0001f195 First run — agent will explore the website from scratch")

    print()
    print("The agent will now autonomously:")
    print("  1. Fetch the Natuurpunt website")
    print("  2. Figure out the HTML structure")
    print("  3. Extract Vlaams-Brabant events")
    print("  4. Generate an .ics calendar file")
    print()
    print("Sit back and watch it work...\n")

    system_prompt = build_system_prompt()

    messages = [
        {
            "role": "user",
            "content": (
                "Please fetch the Natuurpunt agenda for Vlaams-Brabant, figure out "
                "the page structure, extract upcoming events, and generate an .ics "
                "calendar file I can import into Google Calendar or Apple Calendar. "
                "Save it as 'natuurpunt_vlaams_brabant.ics'. Go!"
            ),
        }
    ]

    loop_count = 0
    max_loops = 20

    while loop_count < max_loops:
        loop_count += 1
        print(f"--- Agent step {loop_count}/{max_loops} ---")

        response = client.messages.create(
            model=MODEL,
            max_tokens=4096,
            system=system_prompt,
            tools=TOOLS,
            messages=messages,
        )

        # ── Display Claude's thinking and tool calls ─────────────────────
        for block in response.content:
            if block.type == "text":
                text = block.text.strip()
                if text:
                    for line in text.split("\n"):
                        print(f"  \U0001f916 {line}")

            elif block.type == "tool_use":
                tool_name = block.name
                tool_input = block.input

                if tool_name == "run_python_code":
                    code = tool_input.get("code", "")
                    print(f"\n  \U0001f527 Running Python code ({len(code)} chars)...")
                elif tool_name == "fetch_url":
                    print(f"\n  \U0001f310 Fetching: {tool_input.get('url', '?')}")
                elif tool_name == "fetch_url_selenium":
                    print(f"\n  \U0001f310 Fetching (Selenium): {tool_input.get('url', '?')}")
                elif tool_name == "save_notes":
                    print(f"\n  \U0001f9e0 Saving notes for next run...")
                elif tool_name == "save_working_script":
                    print(f"\n  \U0001f4be Saving standalone script...")
                else:
                    print(f"\n  \U0001f527 Tool call: {tool_name}")

        # ── If done, show results and exit ───────────────────────────────
        if response.stop_reason == "end_turn":
            print(f"\n\u2705 Agent finished in {loop_count} steps.")

            ics_path = Path("natuurpunt_vlaams_brabant.ics")
            if ics_path.exists():
                size = ics_path.stat().st_size
                content = ics_path.read_text(errors="replace")
                event_count = content.count("BEGIN:VEVENT")
                print(f"\n\U0001f4c5 Calendar file: {ics_path.resolve()}")
                print(f"   Size: {size:,} bytes")
                print(f"   Events: {event_count}")
                print(f"\n   To import:")
                print(f"   \u2022 macOS: Double-click the .ics file")
                print(f"   \u2022 Google Calendar: Settings \u2192 Import & Export \u2192 Import")
            else:
                print("\n\u26a0\ufe0f  No .ics file was generated.")
            return

        # ── Execute tool calls and feed results back ─────────────────────
        if response.stop_reason == "tool_use":
            messages.append({"role": "assistant", "content": response.content})

            tool_results = []
            for block in response.content:
                if block.type == "tool_use":
                    # Robust tool execution with error handling
                    try:
                        if block.name in TOOL_FUNCTIONS:
                            func = TOOL_FUNCTIONS[block.name]
                            result = func(**block.input)
                        else:
                            result = {"error": f"Unknown tool: {block.name}"}
                    except TypeError as e:
                        result = {
                            "error": f"Bad parameters for {block.name}: {e}",
                            "input_received": str(block.input)[:200],
                        }
                    except Exception as e:
                        result = {"error": f"Tool execution failed: {e}"}

                    # Print a summary of the result
                    if block.name in ("fetch_url", "fetch_url_selenium"):
                        if "error" in result:
                            print(f"  \u274c {result['error']}")
                        else:
                            print(f"  \u2705 Got {result['length']:,} bytes (status {result['status']})")
                    elif block.name == "run_python_code":
                        if result.get("success"):
                            stdout = result.get("stdout", "").strip()
                            if stdout:
                                for line in stdout.split("\n")[:15]:
                                    print(f"     {line}")
                                if stdout.count("\n") > 15:
                                    print(f"     ... ({stdout.count(chr(10)) - 15} more lines)")
                            if result.get("ics_file_created"):
                                print(f"  \U0001f4c4 .ics file created! ({result['event_count']} events)")
                        else:
                            print(f"  \u274c Code error: {result.get('error', '?')}")
                            tb = result.get("traceback", "")
                            if tb:
                                for line in tb.strip().split("\n")[-5:]:
                                    print(f"     {line}")
                    elif block.name == "save_notes":
                        if result.get("success"):
                            print(f"  \u2705 Notes saved! ({result['total_entries']} total entries in memory)")
                        else:
                            print(f"  \u274c Failed to save notes: {result.get('error', '?')}")
                    elif block.name == "save_working_script":
                        if result.get("success"):
                            print(f"  \u2705 Standalone script saved: {result['path']}")
                            print(f"     Run it directly: python scrape_natuurpunt.py")
                        else:
                            print(f"  \u274c Failed to save script: {result.get('error', '?')}")
                    else:
                        print(f"  Result: {str(result)[:200]}")

                    tool_results.append({
                        "type": "tool_result",
                        "tool_use_id": block.id,
                        "content": json.dumps(result, default=str)[:15000],
                    })

            messages.append({"role": "user", "content": tool_results})

    print("\n\u26a0\ufe0f  Agent hit the maximum step limit. It may not have finished.")


