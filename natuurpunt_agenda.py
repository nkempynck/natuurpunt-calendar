NOTES_FILE = Path("agent_notes.json")


def load_notes() -> dict:
    """Load the agent's memory from previous runs."""
    if NOTES_FILE.exists():
        try:
            return json.loads(NOTES_FILE.read_text())
        except (json.JSONDecodeError, IOError):
            return {}
    return {}


def save_notes(notes: str) -> dict:
    """
    Save notes for future runs. Claude calls this to remember what worked.
    Notes are appended to a list with a timestamp.
    """
    from datetime import datetime

    memory = load_notes()
    if "entries" not in memory:
        memory["entries"] = []

    memory["entries"].append({
        "timestamp": datetime.now().isoformat(),
        "notes": notes,
    })

    # Keep only the last 10 entries to avoid bloat
    memory["entries"] = memory["entries"][-10:]

    NOTES_FILE.write_text(json.dumps(memory, indent=2, ensure_ascii=False))
    return {"success": True, "total_entries": len(memory["entries"])}


def save_working_script(code: str) -> dict:
    """
    Save the working Python code as a standalone script.
    This script can be run directly without the agent or API key.
    """
    script_path = Path("scrape_natuurpunt.py")
    header = '''"""
Natuurpunt Vlaams-Brabant — Standalone Scraper
================================================
Auto-generated by the agent. Run this directly to update the calendar
without needing the Anthropic API.

Usage:
    source venv/bin/activate
    python scrape_natuurpunt.py
"""

'''
    script_path.write_text(header + code, encoding="utf-8")
    return {"success": True, "path": str(script_path.resolve())}


# ── Tools the agent can use ─────────────────────────────────────────────────

def fetch_url(url: str) -> dict:
    """Fetch a URL and return the HTML content."""
    try:
        resp = requests.get(url, headers=HEADERS, timeout=20)
        resp.raise_for_status()
        html = resp.text
        if len(html) > 80_000:
            html = html[:80_000] + "\n\n... [TRUNCATED — page was very long] ..."
        return {"status": resp.status_code, "length": len(resp.text), "html": html}
    except requests.RequestException as e:
        return {"error": str(e)}


def fetch_url_selenium(url: str) -> dict:
    """Fetch a URL using Selenium (for JavaScript-rendered pages)."""
    try:
        from selenium import webdriver
        from selenium.webdriver.chrome.options import Options
        from selenium.webdriver.support.ui import WebDriverWait
        from selenium.webdriver.support import expected_conditions as EC
        from selenium.webdriver.common.by import By
    except ImportError:
        return {"error": "Selenium is not installed. Install with: pip install selenium"}

    try:
        options = Options()
        options.add_argument("--headless")
        options.add_argument("--no-sandbox")
        options.add_argument("--disable-dev-shm-usage")
        driver = webdriver.Chrome(options=options)
        driver.get(url)

        try:
            WebDriverWait(driver, 10).until(
                EC.presence_of_element_located(
                    (By.CSS_SELECTOR, "article, .views-row, .view-content, .agenda-item")
                )
            )
        except Exception:
            pass

        import time
        time.sleep(2)

        html = driver.page_source
        driver.quit()

        if len(html) > 80_000:
            html = html[:80_000] + "\n\n... [TRUNCATED] ..."
        return {"status": 200, "length": len(html), "html": html}
    except Exception as e:
        return {"error": str(e)}


def run_python_code(code: str) -> dict:
    """Execute Python code and return the output."""
    exec_globals = {"__builtins__": __builtins__}

    import io
    from contextlib import redirect_stdout, redirect_stderr

    stdout_capture = io.StringIO()
    stderr_capture = io.StringIO()

    try:
        with redirect_stdout(stdout_capture), redirect_stderr(stderr_capture):
            exec(code, exec_globals)

        result = {"success": True, "stdout": stdout_capture.getvalue()}
        stderr_text = stderr_capture.getvalue()
        if stderr_text:
            result["stderr"] = stderr_text

        ics_path = Path("natuurpunt_vlaams_brabant.ics")
        if ics_path.exists():
            result["ics_file_created"] = True
            result["ics_file_size"] = ics_path.stat().st_size
            content = ics_path.read_text(errors="replace")
            result["event_count"] = content.count("BEGIN:VEVENT")

        return result

    except Exception as e:
        return {
            "success": False,
            "error": str(e),
            "traceback": traceback.format_exc(),
            "stdout": stdout_capture.getvalue(),
        }


